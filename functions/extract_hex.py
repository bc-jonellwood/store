def generate_sql_updates(strings):

    sql_updates = []
    for string in strings:
        # Split the string by commas
        parts = string.split(',')
        # print(len(parts))
        if len(parts) == 6:

            color_id, color_name, p_hex, s_hex, t_hex, unk = parts
            # Remove leading and trailing whitespace
            color_id = color_id.strip()
            # print(color_id)
            color_name = color_name.strip()
            # print(color_name)
            p_hex = p_hex.strip()
            # print(p_hex)
            s_hex = s_hex.strip()
            # print(s_hex)
            t_hex = t_hex.strip()
            # print(t_hex)
            unk = unk.strip()
            # Check if s_hex and t_hex are empty and set them to NULL if needed
            if not s_hex:
                s_hex = 'NULL'
            if not t_hex:
                t_hex = 'NULL'
            # Construct the SQL update statement
            sql_update = f"UPDATE colors SET p_hex = '{p_hex}', s_hex = '{s_hex}', t_hex = '{t_hex}' WHERE color_id = '{color_id}';"
            sql_updates.append(sql_update)
    return sql_updates



def extract_hex_codes(strings):
    hex_codes = []
    for string in strings:
        index = string.find('#')
        if index != -1:
            hex_code = string[index:]
            hex_codes.append(hex_code)
    return hex_codes


def write_to_file(hex_codes, filename):
    with open(filename, 'w') as file:
        for hex_code in hex_codes:
            file.write(hex_code + '\n')


